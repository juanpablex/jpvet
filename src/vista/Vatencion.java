    public DefaultTableModel getModel(ArrayList<Mmascota> lista) {
        String[] titulos = {"id", "due√±o", "mascota", "genero",
            "fec nac", "edad", "animal", "raza", "peso"};
        DefaultTableModel modelo = new DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        Object[] fila = new Object[9];
        for (Mmascota p : lista) {
            fila[0] = p.getId();
            fila[1] = p.getDuenio();
            fila[2] = p.getNombre();
            fila[3] = p.getGenero();
            fila[4] = p.getFechanac();
            fila[5] = p.getEdad();
            fila[6] = p.getAnimal();
            fila[7] = p.getRaza();
            fila[8] = p.getPeso();
            modelo.addRow(fila);
        }
        return modelo;
    }

